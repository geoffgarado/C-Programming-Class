#include <iostream>

int main(){
  int myNum = 5; // Integer (Whole Number)
  float myFlatNum = 5.99; // Floating point Number
  double myDoubleNum = 9.98; // Floating point Number
  char myLetter = 'D'; // Character
  bool myBoolean = true; // Boolean
  string myText = "Hello"; // String
}

/*
  Data Type     Size            Description
  Boolean       1 byte          - stores true or false values
  char          1 byte          - stores a single character/letter/number, or ASCII values
  int           2 or 4 bytes    - stores whole numbers, without decimals
  float         4 bytes         - stores fractional numbers, containing 1 or more decimals. Sufficient for storing 7 decimal digits
  double        8 bytes         - stores fractional numbers, containing 1 or more decimals. Sufficient for storing 15 decimal digits
*/
